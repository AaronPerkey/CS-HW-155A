package com.fmt;
/**
 * 
 * Models an individual store.
 * 
 * @author aaron
 *
 */
public class Store {
	
	private final String storeIdentification;
	private final Person person;
	private final Location address;
	
	public Store(String storeIdentification, Person person, Location address) {
		super();
		this.storeIdentification = storeIdentification;
		this.person = person;
		this.address = address;
	}

	public String getStoreIdentification() {
		return storeIdentification;
	}

	public void setStoreIdentification(String storeIdentification) {
		this.storeIdentification = storeIdentification;
	}

	public Person getPerson() {
		return person;
	}

	public void setPerson(Person person) {
		this.person = person;
	}

	public Location getAddress() {
		return address;
	}

	public void setAddress(Location address) {
		this.address = address;
	}
	
	
	
}
